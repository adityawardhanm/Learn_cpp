// if - else statement
#include <iostream>

int main() {
    if (2 > 1){
        std::cout << "2 is greater than 1\n";
    } 
    if (2 == 2){
        std::cout << "2 is equal to 1\n";
    }
    else if (2 < 1){
        std::cout << "2 is less than 1\n";
    }
    else {
        std::cout << "2 is not greater than 1\n";
    }
}